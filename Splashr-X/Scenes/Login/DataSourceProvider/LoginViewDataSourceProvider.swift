import UIKit

class LoginViewDataSourceProvider<Model: UIImage>: NSObject, CollectionViewDataSourceProvider {
  
  private let models: [Model]
  
  required init(models: [Model]) {
    self.models = models
  }
  
  // MARK: - DataSource
  
  func collectionView(_ collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
    return models.count
  }
  
  func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
    let cell = collectionView.dequeue(LoginImagePreviewCollectionViewCell.self, for: indexPath)
    let photo = models[indexPath.row]
    
    let targetSize = CGSize(width: collectionView.frame.width, height: collectionView.frame.height)
    cell.photoImageView.image = resizeImage(image: photo, targetSize: targetSize)
    
    return cell
  }
  
  func resizeImage(image: UIImage, targetSize: CGSize) -> UIImage? {
    let size = image.size
    
    let widthRatio  = targetSize.width  / size.width
    let heightRatio = targetSize.height / size.height
    
    // Figure out what our orientation is, and use that to form the rectangle
    var newSize: CGSize
    if(widthRatio > heightRatio) {
      newSize = CGSize(width: size.width * heightRatio, height: size.height * heightRatio)
    } else {
      newSize = CGSize(width: size.width * widthRatio, height: size.height * widthRatio)
    }
    
    // This is the rect that we've calculated out and this is what is actually used below
    let rect = CGRect(x: 0, y: 0, width: newSize.width, height: newSize.height)
    
    // Actually do the resizing to the rect using the ImageContext stuff
    UIGraphicsBeginImageContextWithOptions(newSize, false, 1.0)
    image.draw(in: rect)
    let newImage = UIGraphicsGetImageFromCurrentImageContext()
    UIGraphicsEndImageContext()
    
    return newImage
  }

}
